#!/usr/bin/env node
// Fetch real, recent, community-verified iNaturalist observations for every
// creature in src/data/creatures.ts and write the derived facts (not raw API
// payloads) to src/data/sightings.ts. See
// ~/setup-mac/.agents/skills/inaturalist-api/SKILL.md for API details and
// the ethical/accuracy notes this script follows (no exact pins, no "not
// seen" claims, research-grade + non-captive only).

import { readFileSync, renameSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const creaturesPath = path.join(projectRoot, "src/data/creatures.ts");
const outPath = path.join(projectRoot, "src/data/sightings.ts");

const BASE = "https://api.inaturalist.org/v1";
const HEADERS = {
  "User-Agent": "maui-field-guide-sightings/1.0 (family trip planning site)",
};
const MANA_KAI = { lat: 20.7049, lng: -156.4465 };
const RADIUS_KM = 50; // covers all of Maui island from Mana Kai
const ONE_YEAR_AGO = new Date(Date.now() - 365 * 86_400_000)
  .toISOString()
  .slice(0, 10);

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function getJSON(url) {
  const res = await fetch(url, { headers: HEADERS });
  if (!res.ok) throw new Error(`${res.status} ${res.statusText} for ${url}`);
  return res.json();
}

function extractCreatures() {
  const src = readFileSync(creaturesPath, "utf8");
  const start = src.indexOf("export const creatures");
  const end = src.indexOf("] satisfies Creature[]");
  const chunk = src.slice(start, end);
  const ids = [...chunk.matchAll(/id: "([^"]+)"/g)].map((m) => m[1]);
  const names = [...chunk.matchAll(/scientificName: "([^"]+)"/g)].map(
    (m) => m[1],
  );
  return ids.map((id, i) => ({ id, scientificName: names[i] }));
}

async function resolveTaxonId(scientificName) {
  const url = `${BASE}/taxa?${new URLSearchParams({
    q: scientificName,
    rank: "species,subspecies",
    per_page: "1",
  })}`;
  const data = await getJSON(url);
  return data.results?.[0]?.id;
}

async function fetchSighting(creature) {
  const commonParams = {
    lat: String(MANA_KAI.lat),
    lng: String(MANA_KAI.lng),
    radius: String(RADIUS_KM),
    quality_grade: "research",
    captive: "false",
  };

  const mostRecentUrl = `${BASE}/observations?${new URLSearchParams({
    ...commonParams,
    taxon_name: creature.scientificName,
    order_by: "observed_on",
    order: "desc",
    per_page: "1",
  })}`;
  let recent = await getJSON(mostRecentUrl);

  let taxonId;
  if (!recent.results?.length) {
    // taxon_name didn't resolve directly; fall back to a taxon search.
    taxonId = await resolveTaxonId(creature.scientificName);
    if (!taxonId) return { creatureId: creature.id, recentObservations: 0 };
    await sleep(1100);
    const byTaxonUrl = `${BASE}/observations?${new URLSearchParams({
      ...commonParams,
      taxon_id: String(taxonId),
      order_by: "observed_on",
      order: "desc",
      per_page: "1",
    })}`;
    recent = await getJSON(byTaxonUrl);
  }

  if (!recent.results?.length) {
    return { creatureId: creature.id, recentObservations: 0 };
  }

  const top = recent.results[0];
  await sleep(1100);

  const countParams = taxonId
    ? { ...commonParams, taxon_id: String(taxonId) }
    : { ...commonParams, taxon_name: creature.scientificName };
  const countUrl = `${BASE}/observations?${new URLSearchParams({
    ...countParams,
    d1: ONE_YEAR_AGO,
    per_page: "0",
  })}`;
  const countData = await getJSON(countUrl);

  return {
    creatureId: creature.id,
    recentObservations: countData.total_results ?? 0,
    mostRecentDate: top.observed_on ?? undefined,
    mostRecentPlaceGuess: top.place_guess ?? undefined,
    observationUrl: top.uri ?? undefined,
    radiusKm: RADIUS_KM,
    generatedAt: new Date().toISOString().slice(0, 10),
  };
}

async function main() {
  const creatures = extractCreatures();
  const sightings = [];
  const failures = [];
  for (const creature of creatures) {
    process.stderr.write(
      `Fetching ${creature.id} (${creature.scientificName})...\n`,
    );
    try {
      const sighting = await fetchSighting(creature);
      sightings.push(sighting);
    } catch (err) {
      process.stderr.write(`  failed: ${err.message}\n`);
      failures.push(creature.id);
    }
    await sleep(1100);
  }

  if (failures.length > 0) {
    throw new Error(
      `Refusing to replace cached sightings after ${failures.length} API failure(s): ${failures.join(
        ", ",
      )}`,
    );
  }

  const generatedAt = new Date().toISOString().slice(0, 10);
  const lines = [
    "// Generated by scripts/fetch-inaturalist-sightings.mjs — do not hand-edit.",
    "// Source: iNaturalist public API (api.inaturalist.org/v1), research-grade,",
    "// non-captive observations within 50km of Mana Kai Maui. Counts and dates",
    "// are community evidence of recent regional activity, not a sighting",
    "// guarantee or an exact location.",
    'import type { CreatureSighting } from "./types";',
    "",
    `export const sightingsGeneratedAt = "${generatedAt}";`,
    "",
    "export const creatureSightings = [",
    ...sightings.map((s) => {
      const fields = [`    creatureId: "${s.creatureId}"`];
      fields.push(`    recentObservations: ${s.recentObservations}`);
      if (s.mostRecentDate)
        fields.push(`    mostRecentDate: "${s.mostRecentDate}"`);
      if (s.mostRecentPlaceGuess)
        fields.push(
          `    mostRecentPlaceGuess: ${JSON.stringify(s.mostRecentPlaceGuess)}`,
        );
      if (s.observationUrl)
        fields.push(`    observationUrl: "${s.observationUrl}"`);
      if (s.radiusKm) fields.push(`    radiusKm: ${s.radiusKm}`);
      if (s.generatedAt) fields.push(`    generatedAt: "${s.generatedAt}"`);
      return `  {\n${fields.join(",\n")},\n  },`;
    }),
    "] satisfies CreatureSighting[];",
    "",
  ];

  const tempPath = `${outPath}.tmp`;
  writeFileSync(tempPath, lines.join("\n"));
  renameSync(tempPath, outPath);
  process.stderr.write(`Wrote ${sightings.length} sightings to ${outPath}\n`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
